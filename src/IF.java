package src;

public class IF {
    public static void main(String[] args) {
        boolean isAlien = false;
        if (!isAlien) {
            System.out.println("It is not an alien!");
            System.out.println("And I am scared of aliens!");
        }
    }
}

//explain this basic code
//The IF class is a simple Java program that demonstrates the use of the if statement.
//The main method is the entry point of the program, and it is where the program starts executing.
//The boolean variable isAlien is set to false.
//The if statement checks if the value of isAlien is false using the ! (logical NOT) operator.
//If isAlien is false, the program prints the messages "It is not an alien!" and "And I am scared of aliens!" to the console.
//When the program is run, the messages will be displayed in the console.
//The output will be:
//It is not an alien!
//And I am scared of aliens!
//The program demonstrates the use of the if statement to conditionally execute code based on a boolean condition.
//The logical NOT operator (!) is used to negate the value of the boolean variable.
//If the condition is true, the code block inside the if statement is executed. Otherwise, it is skipped.
//This allows for control flow in the program based on the value of the boolean variable.
//The messages printed to the console provide additional context and output for the program.
//Overall, the program showcases the basic usage of the if statement in Java.
